// Using document.write() to output results to the document

// Calculate the absolute value of a number
document.write("Absolute value of -5: = " + Math.abs(-5) + "<br>");

// Round up a number to the nearest integer
document.write("Ceiling of 4.3: = " + Math.ceil(4.3) + "<br>");

// Round down a number to the nearest integer
document.write("Floor of 4.7: = " + Math.floor(4.7) + "<br>");

// Find the maximum of two numbers
document.write("Maximum of 10 and 20: = " + Math.max(10, 20) + "<br>");

// Find the minimum of two numbers
document.write("Minimum of 10 and 20: = " + Math.min(10, 20) + "<br>");

// Calculate the power of a number
document.write("2 to the power of 3: = " + Math.pow(2, 3) + "<br>");

// Calculate the square root of a number
document.write("Square root of 16: = " + Math.sqrt(16) + "<br>");

// Generate a random number between 0 and 1
document.write("Random number between 0 and 1: = " + Math.random()+'<br>');

//math round above.5 make it next integer
document.write("Round Number 5.70 = "+Math.round(4.70));